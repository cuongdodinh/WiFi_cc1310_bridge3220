char HexToChar (char b)
{
    if (b <= '9')
        return b - '0';
    else
        return 10 + b - 'A';
}

char hex [16] = "0123456789ABCDEF";

//------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
_u8 ToHexString (_u8* byteArrayBuff, _u8 len, char* hexStringBuff)
{
    int s;
    int len2 = len * 2;

    for (s = 0; s < len2; s += 2)
    {
        hexStringBuff [s] = hex [*byteArrayBuff >> 4];
        hexStringBuff [s + 1] = hex [*byteArrayBuff & 0x0F];
        byteArrayBuff++;
    }

    return len2;
}

_u16 crcTable [256];

//--------------------------------------------------------------------------------------------------------------------------
void MakeCRC16Table(void)
{
  _u16 r;
  int s, s1;

  for(s = 0; s < 256; s++)
  {
    r = ((_u16)s)<<8;

    for (s1 = 0; s1 < 8; s1++)
    {
    if (r&(1<<15))
        r = (r<<1)^0x8005;
    else
        r = r<<1;
    }
     crcTable[s] = r;
   }
}

//--------------------------------------------------------------------------------------------------------------------------
_u16 GetCRC16(_u8 *buf, _u16 len)
{
    _u16 crc;
    crc = 0xFFFF;
    while (len--)
    {
        crc = crcTable [((crc>>8)^*buf++)&0xFF] ^ (crc<<8);
    }
    crc ^= 0xFFFF;

    return crc;
}
